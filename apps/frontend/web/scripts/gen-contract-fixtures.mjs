#!/usr/bin/env node
// Regenerate the cross-module contract fixtures (web/src/__fixtures__/contracts/*.json) from the
// LIVE BFF. The dev server proxies the BFF; point this at that base URL (default the dev proxy).
//
//   BFF_BASE=http://127.0.0.1:5190 node scripts/gen-contract-fixtures.mjs
//   npm run gen:contract-fixtures
//
// The analytics/coverage payloads are huge, so they are trimmed to a few maturities/expiries/points
// while keeping EVERY key (the shape must stay complete, only the cardinality shrinks). The IBKR
// status fixture is NOT fetched here: /api/ibkr/status is not mounted on every BFF build and is
// unreachable via the offline proxy, so it is generated in-process by the Python contract test
// (apps/frontend/tests/test_frontend_ts_contract.py). A stale fixture that passes is worthless,
// so re-run this whenever a serializer changes shape.

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const BASE = process.env.BFF_BASE ?? "http://127.0.0.1:5190";
const OUT = resolve(dirname(fileURLToPath(import.meta.url)), "../src/__fixtures__/contracts");
mkdirSync(OUT, { recursive: true });

async function getJson(path) {
  const res = await fetch(`${BASE}${path}`);
  if (!res.ok) throw new Error(`GET ${path} -> ${res.status}`);
  return res.json();
}

function write(name, obj) {
  writeFileSync(resolve(OUT, name), `${JSON.stringify(obj, null, 2)}\n`);
  console.log(`wrote ${name}`);
}

function trimAnalytics(a) {
  a.maturities = a.maturities.slice(0, 2).map((m) => {
    m.points = m.points.slice(0, 3);
    for (const k of ["deltas", "implied_vols", "log_moneyness", "moneyness_buckets"]) {
      if (Array.isArray(m.smile?.[k])) m.smile[k] = m.smile[k].slice(0, 4);
    }
    return m;
  });
  a.n_maturities = a.maturities.length;
  if (a.surface) {
    a.surface.log_moneyness = a.surface.log_moneyness.slice(0, 4);
    a.surface.maturity_years = a.surface.maturity_years.slice(0, 2);
    a.surface.implied_vol = a.surface.implied_vol.slice(0, 2).map((r) => r.slice(0, 4));
  }
  return a;
}

function trimCoverage(c) {
  c.expiries = c.expiries.slice(0, 3);
  c.tenors = c.tenors.slice(0, 3);
  c.n_expiries = c.expiries.length;
  return c;
}

const underlying = process.env.CONTRACT_UNDERLYING ?? "SX5E";

write(`analytics.${underlying}.json`, trimAnalytics(await getJson(`/api/analytics?underlying=${underlying}`)));
write(`coverage.${underlying}.json`, trimCoverage(await getJson(`/api/coverage?underlying=${underlying}`)));
write("health.json", await getJson("/api/health"));
write("indices.json", await getJson("/api/indices"));
write("providers.json", await getJson("/api/providers"));

console.log("done. ibkr_status.json is generated by the python contract test.");
